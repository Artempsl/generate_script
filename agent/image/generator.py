"""
agent/image/generator.py

Generates cinematic images for each script segment using the Pollinations.ai
authenticated API (gen.pollinations.ai).

Key design decisions:
- Prompts generated by LLM based on actual segment text (not keyword matching)
- Sequential generation (no threading) — simple, predictable, rate-limit safe
- Fallback model chain: flux → nanobanana → kontext
  Every segment MUST produce an image; only move to the next model if all
  retries on the current one are exhausted.
- 1920×1080 resolution (16:9 Full HD)
- API key read from environment variable 'pollination_api'
- Prompts saved to {project_dir}/images/image_prompts.json
"""

import json
import logging
import os
import time
import urllib.parse
from pathlib import Path

import requests

# ---------------------------------------------------------------------------
# Constants
# ---------------------------------------------------------------------------

BASE_URL = "https://gen.pollinations.ai"
IMAGE_WIDTH = 1920
IMAGE_HEIGHT = 1080
FALLBACK_MODELS = ["z-image-turbo", "p-image", "flux", "gpt-image-1-mini"]

RETRIES_PER_MODEL = 3
RETRY_DELAY = 5        # seconds between retries within the same model
SEGMENT_DELAY = 2      # seconds between segments
RATE_LIMIT_WAIT = 30   # seconds to wait on 429
LLM_PROMPT_TEMPERATURE = 0.4
LLM_PROMPT_MODEL = "gpt-5.4"  # model used for image prompt generation
BALANCE_WAIT = 60      # seconds to wait when balance is insufficient (402)
STYLE_SUFFIX = (
    "Image style description: Horizontal wide-screen, 16:9 aspect ratio, realistic physics, "
    "realistic geometry, real-life snapshot, ultra-realistic, photorealistic, real-world textures, "
    "physically accurate lighting, soft shadows, realistic reflections, true-to-life proportions, "
    "not staged, not over-posed."
)


class InsufficientBalanceError(Exception):
    """Raised when the Pollinations API returns 402 (not enough pollen)."""

logger = logging.getLogger(__name__)


